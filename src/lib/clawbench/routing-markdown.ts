import { MODELS, NEBIUS_CATALOG, TASK_TYPES, STRATEGIES } from "./constants";

export type RoutingRule = {
  id: string;
  task_type: string;
  primary_model: string;
  fallback_model: string | null;
  strategy: string | null;
  confidence_threshold: number | null;
  escalation_condition: string | null;
  supporting_eval_count: number;
  updated_at: string;
};

function modelDisplay(id: string | null | undefined): string {
  if (!id) return "—";
  const m =
    MODELS.find((x) => x.id === id) ??
    NEBIUS_CATALOG.find((x) => x.id === id);
  return m ? `${m.name} (\`${id}\`)` : `\`${id}\``;
}

function taskLabel(id: string): string {
  return TASK_TYPES.find((t) => t.id === id)?.label ?? id;
}

function strategyLabel(id: string | null): string {
  if (!id) return "—";
  return STRATEGIES.find((s) => s.id === id)?.label ?? id;
}

export function buildRoutingMarkdown(rules: RoutingRule[]): string {
  const generatedAt = new Date().toISOString();
  const lines: string[] = [];

  lines.push(
    `# Routing Rules`,
    ``,
    `> Generated by ClawBench on ${generatedAt}.`,
    `> Paste this file into your coding agent (Cursor, Claude Code, Lovable, etc.) so it routes prompts to the best model per task type.`,
    ``,
    `## How to use this file`,
    ``,
    `1. Detect the **task type** of the incoming user request (one of: ${TASK_TYPES.map((t) => `\`${t.id}\``).join(", ")}).`,
    `2. Look up the matching rule in the table below.`,
    `3. Call the **primary model** first.`,
    `4. If the response confidence is below the rule's threshold, or the **escalation condition** matches the response, retry with the **fallback model**.`,
    `5. If no rule matches the detected task type, default to the rule for \`coding\` (or your own baseline).`,
    ``,
  );

  if (rules.length === 0) {
    lines.push(
      `_No routing rules defined yet. Run evals in ClawBench and save the winners as routing rules to populate this file._`,
      ``,
    );
    return lines.join("\n");
  }

  // Summary table
  lines.push(
    `## Summary`,
    ``,
    `| Task type | Primary model | Fallback model | Strategy | Confidence threshold |`,
    `| --- | --- | --- | --- | --- |`,
  );
  for (const r of rules) {
    lines.push(
      `| ${taskLabel(r.task_type)} | ${modelDisplay(r.primary_model)} | ${modelDisplay(r.fallback_model)} | ${strategyLabel(r.strategy)} | ${r.confidence_threshold ?? "—"} |`,
    );
  }
  lines.push(``);

  // Per-rule detail
  lines.push(`## Rules`, ``);
  for (const r of rules) {
    lines.push(
      `### ${taskLabel(r.task_type)} (\`${r.task_type}\`)`,
      ``,
      `- **Primary model:** ${modelDisplay(r.primary_model)}`,
      `- **Fallback model:** ${modelDisplay(r.fallback_model)}`,
      `- **Strategy:** ${strategyLabel(r.strategy)}`,
      `- **Confidence threshold:** ${r.confidence_threshold ?? "—"}`,
      `- **Escalation condition:** ${r.escalation_condition?.trim() ? r.escalation_condition.trim() : "_none_"}`,
      `- **Supporting evals:** ${r.supporting_eval_count}`,
      ``,
    );
  }

  // Decision algorithm
  lines.push(
    `## Decision algorithm`,
    ``,
    "```text",
    `function route(taskType, prompt):`,
    `  rule = rules[taskType] or rules["coding"]`,
    `  response = call(rule.primary_model, prompt)`,
    `  if response.confidence < rule.confidence_threshold`,
    `     or matches(response, rule.escalation_condition):`,
    `    response = call(rule.fallback_model, prompt)`,
    `  return response`,
    "```",
    ``,
    `_Generated by [ClawBench](https://ai-agent-balancer.lovable.app) — agent eval control plane._`,
    ``,
  );

  return lines.join("\n");
}
